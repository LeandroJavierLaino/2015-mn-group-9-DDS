function Caracteristica() {
	this.nombre = null;
	this.cantidad = null;
	this.tipo = null;
}
function Condimento(nombre, cantidad, tipo) {
	this.nombre = nombre;
	this.cantidad = cantidad;
	this.tipo = tipo;
}
function Ingrediente(nombre, cantidad, tipo) {
	this.nombre = nombre;
	this.cantidad = cantidad;
	this.tipo = tipo;
}